// ─── MAIN SCRIPT ──────────────────────────────────────────────────────────────

function main() {
  const batchFile = path.join(DATA_DIR, "_batch1_drugs.json");
  const batch = JSON.parse(fs.readFileSync(batchFile, "utf8"));

  const files = {
    main: {
      path: path.join(DATA_DIR, "formulary_data_ABDM_FHIR.json"),
      data: null,
      modified: false,
    },
    generics: {
      path: path.join(DATA_DIR, "formulary_data_ABDM_FHIR_generics.json"),
      data: null,
      modified: false,
    },
    orphans: {
      path: path.join(DATA_DIR, "formulary_data_ABDM_FHIR_orphans.json"),
      data: null,
      modified: false,
    },
  };

  // Load all files
  for (const key of Object.keys(files)) {
    files[key].data = JSON.parse(fs.readFileSync(files[key].path, "utf8"));
  }

  let updated = 0;
  let skipped = 0;
  let notInDB = 0;

  for (const drugName of batch) {
    // Find in DOSING_DB (try exact match, then case-insensitive)
    let dosingBands = DOSING_DB[drugName];
    if (!dosingBands) {
      // Try case-insensitive
      for (const key of Object.keys(DOSING_DB)) {
        if (key.toLowerCase() === drugName.toLowerCase()) {
          dosingBands = DOSING_DB[key];
          break;
        }
      }
    }

    if (!dosingBands || dosingBands.length === 0) {
      notInDB++;
      console.log(`  [SKIP] ${drugName} — no dosing data in DB`);
      continue;
    }

    // Find in formulary files
    let found = false;
    for (const key of Object.keys(files)) {
      const idx = files[key].data.findIndex((d) => {
        return (
          d.generic_name === drugName ||
          d.generic_name.toLowerCase() === drugName.toLowerCase() ||
          d.generic_name.toUpperCase() === drugName.toUpperCase()
        );
      });

      if (idx >= 0) {
        const existing = files[key].data[idx].dosing_bands;
        if (existing && existing.length > 0) {
          skipped++;
          console.log(
            `  [EXISTS] ${drugName} — already has ${existing.length} bands in ${key}`,
          );
        } else {
          files[key].data[idx].dosing_bands = dosingBands;
          files[key].modified = true;
          updated++;
          console.log(
            `  [OK] ${drugName} — added ${dosingBands.length} dosing bands in ${key}`,
          );
        }
        found = true;
        break;
      }
    }

    if (!found) {
      console.log(`  [ERROR] ${drugName} — not found in any formulary file`);
    }
  }

  // Save modified files
  for (const key of Object.keys(files)) {
    if (files[key].modified) {
      fs.writeFileSync(
        files[key].path,
        JSON.stringify(files[key].data, null, 2),
        "utf8",
      );
      console.log(`\nSaved: ${files[key].path}`);
    }
  }

  console.log(`\n=== SUMMARY ===`);
  console.log(`Total batch drugs: ${batch.length}`);
  console.log(`Updated with dosing bands: ${updated}`);
  console.log(`Already had dosing bands: ${skipped}`);
  console.log(`No dosing data available: ${notInDB}`);
  console.log(
    `Coverage: ${(((updated + skipped) / batch.length) * 100).toFixed(1)}%`,
  );
}

main();
