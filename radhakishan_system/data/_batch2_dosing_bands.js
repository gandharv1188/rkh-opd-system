function main() {
  const files = [
    { name: "formulary_data_ABDM_FHIR.json", label: "branded" },
    { name: "formulary_data_ABDM_FHIR_generics.json", label: "generics" },
    { name: "formulary_data_ABDM_FHIR_orphans.json", label: "orphans" },
  ];

  const batch2 = JSON.parse(
    fs.readFileSync(path.join(DATA_DIR, "_batch2_drugs.json"), "utf8"),
  );

  let totalPatched = 0;
  let totalBands = 0;
  let skipped = [];

  for (const file of files) {
    const filePath = path.join(DATA_DIR, file.name);
    const data = JSON.parse(fs.readFileSync(filePath, "utf8"));
    let filePatched = 0;

    for (const drug of data) {
      const gn = drug.generic_name;
      if (!batch2.includes(gn)) continue;

      const bands = DOSING_DATA[gn];
      if (!bands || bands === "already_defined") {
        if (!bands) skipped.push(gn);
        continue;
      }

      if (drug.dosing_bands && drug.dosing_bands.length > 0) {
        console.log(`  SKIP (already has bands): ${gn}`);
        continue;
      }

      drug.dosing_bands = bands;
      filePatched++;
      totalBands += bands.length;
    }

    if (filePatched > 0) {
      fs.writeFileSync(filePath, JSON.stringify(data, null, 2), "utf8");
      console.log(`Patched ${filePatched} drugs in ${file.name}`);
    }
    totalPatched += filePatched;
  }

  console.log(`\n=== SUMMARY ===`);
  console.log(`Total drugs patched: ${totalPatched}`);
  console.log(`Total dosing bands added: ${totalBands}`);
  if (skipped.length > 0) {
    console.log(`\nSkipped (no dosing data defined):`);
    skipped.forEach((s) => console.log(`  - ${s}`));
  }
}

main();
