// ============================================================
// APPLY TO FORMULARY FILES
// ============================================================
function applyDosing(filePath) {
  const data = JSON.parse(fs.readFileSync(filePath, "utf8"));
  let updated = 0;
  for (const drug of data) {
    const bands = DOSING[drug.generic_name];
    if (
      bands &&
      Array.isArray(bands) &&
      (!drug.dosing_bands || drug.dosing_bands.length === 0)
    ) {
      drug.dosing_bands = bands;
      updated++;
    }
  }
  if (updated > 0) {
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2), "utf8");
    console.log(`  ${path.basename(filePath)}: updated ${updated} drugs`);
  } else {
    console.log(`  ${path.basename(filePath)}: no updates needed`);
  }
  return updated;
}

